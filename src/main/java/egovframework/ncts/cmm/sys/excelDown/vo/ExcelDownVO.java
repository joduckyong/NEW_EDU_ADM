package egovframework.ncts.cmm.sys.excelDown.vo;

import egovframework.com.vo.CommonVO;

public class ExcelDownVO extends CommonVO {
	private String excelSeq;
	private String menuSeq;
	private String excelCn1;
	
	public String getExcelSeq() {
		return excelSeq;
	}
	public void setExcelSeq(String excelSeq) {
		this.excelSeq = excelSeq;
	}
	public String getMenuSeq() {
		return menuSeq;
	}
	public void setMenuSeq(String menuSeq) {
		this.menuSeq = menuSeq;
	}
	public String getExcelCn1() {
		return excelCn1;
	}
	public void setExcelCn1(String excelCn1) {
		this.excelCn1 = excelCn1;
	}
}